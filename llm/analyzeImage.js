import OpenAI from "openai";

const client = new OpenAI({ apiKey: process.env.OPENAI_API_KEY });

async function analyzeImage(base64Image) {
  const response = await client.responses.create({
    model: "gpt-4.1",
    input: [
      {
        role: "user",
        content: [
          {
            type: "input_text",
            text: `
วิเคราะห์ภาพที่แนบมาเพื่อระบุปัญหาที่เกิดขึ้นจากภาพ

ให้วิเคราะห์ภาพก่อนโดยใช้ตรรกะดังนี้ (ห้ามแสดงขั้นตอนการคิด):
1. ตรวจสอบว่าภาพเป็นประเภทใด เช่น
   - screenshot จากแอป
   - รูปบิล
   - รูปสินค้า / QR code
   - รูปทั่วไป
2. ตรวจสอบว่ามีข้อความ error หรือข้อความแจ้งปัญหาหรือไม่
3. ตรวจสอบว่ามีหลักฐานของปัญหาชัดเจนหรือไม่ เช่น
   - ข้อความแจ้งเตือนในแอป
   - บิลมีการเขียนทับ
   - สถานะคำร้อง
   - ข้อความโปรโมชั่น
4. ถ้าไม่มีหลักฐานของปัญหาในภาพ ห้ามเดา

รายการคำตอบที่อนุญาตให้ตอบได้มีเพียง:

ลงทะเบียนไม่ได้
qrcodeปกติ
รอการอนุมัติคำร้อง
เงินไม่เข้า
ฉลากสินค้า
บิลซ้ำ
บิลปกติ
รองรับเฉพาะบิลพิมพ์
สินค้าไม่ร่วมโปรโมชั่น
สินค้าเกินระยะเวลาโปรโมชั่น
สินค้าเคยถูกส่งมาแล้ว
กล้องเปิดไม่ได้
ไม่อนุมัติ
ผมไม่เข้าใจคำสั่งของคุณ

กฎการตัดสินใจที่สำคัญ:

- ถ้าเป็น screenshot แอป และมีข้อความ error เกี่ยวกับการสมัคร → ลงทะเบียนไม่ได้
- ถ้าเห็น QR code บนสินค้าและไม่มีปัญหา → qrcode ปกติ
- ถ้าบิลมีลายปากกาเขียน → สแกนบิลไม่ได้(บิลมีการเขียน)
- ถ้าเป็นหน้าสถานะคำร้องและขึ้นสถานะรอ → รอการอนุมัติคำร้อง
- ถ้าเป็นรูปบิลธรรมดาและไม่มีปัญหา → บิลปกติ
- ถ้าเป็นเพียงรูปบิลธรรมดาโดยไม่มีข้อความปัญหา → ผมไม่เข้าใจคำสั่งของคุณ
- ถ้าเป็นเพียงรูป QR code ปกติโดยไม่มีปัญหา → ผมไม่เข้าใจคำสั่งของคุณ
- ถ้าไม่พบหลักฐานของปัญหาในภาพ → ผมไม่เข้าใจคำสั่งของคุณ

กฎการตอบ:
- ให้เลือกคำตอบที่ตรงกับภาพมากที่สุดเพียง 1 ข้อ
- ต้องตอบด้วยข้อความจากรายการด้านบนเท่านั้น
- ห้ามอธิบายเพิ่มเติม
- ห้ามเพิ่มข้อความอื่น
- ห้ามใช้ markdown
- ห้ามตอบหลายบรรทัด

ความรู้เพิ่มเติม:
- ฉลากสินค้า คือ ฉลากที่ติดบนสินค้าที่มี QR code + barcode และ labelเป็นสีอยู่บนฉลาก ด้านล่างขาว มีชื่อสินค้า และคำว่า Base และมีคำว่า ปริมาณสุทธิ์

        `,
          },
          {
            type: "input_image",
            image_url: `data:image/jpeg;base64,${base64Image}`,
          },
        ],
      },
    ],
  });

  return response.output_text;
}

export { analyzeImage };
